/** Domains we intentionally allow even if they appear on some public lists. */
const ALLOWLIST = new Set(["gmail.com", "googlemail.com", "outlook.com", "hotmail.com", "yahoo.com", "icloud.com", "proton.me", "protonmail.com", "live.com", "msn.com", "aol.com", "me.com", "mac.com"]);

const SUSPICIOUS_PREFIXES = [
  "temp",
  "tmp",
  "trash",
  "disposable",
  "throwaway",
  "fake",
  "spam",
  "maildrop",
  "guerrilla",
  "yopmail",
  "sharklaser",
];

export function getEmailDomain(email: string): string | null {
  const at = email.lastIndexOf("@");
  if (at < 0) return null;
  return email
    .slice(at + 1)
    .trim()
    .toLowerCase()
    .replace(/\.+$/, "");
}

export function isDisposableEmail(email: string): boolean {
  const domain = getEmailDomain(email);
  if (!domain) return true;
  if (ALLOWLIST.has(domain)) return false;
  if (DISPOSABLE_DOMAINS.has(domain)) return true;

  // Block obvious temp-mail style subdomains / brand domains
  const labels = domain.split(".");
  if (labels.some((part) => SUSPICIOUS_PREFIXES.some((p) => part.includes(p)))) {
    return true;
  }

  return false;
}
